#include "3-calc.h"

/**
 * op_add - addition
 * @a: first operand
 * @b: second operand
 *
 * Return: value of operation
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction
 * @a: first operand
 * @b: second operand
 *
 * Return: value of operation
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 * @a: first operand
 * @b: second operand
 *
 * Return: value of operation
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 * @a: first operand
 * @b: second operand
 *
 * Return: value of operation
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo
 * @a: first operand
 * @b: second operand
 *
 * Return: value of operation
 */
int op_mod(int a, int b)
{
	return (a % b);
}
